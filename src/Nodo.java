public class Nodo {

    int elemento;
    Nodo successivo;

    public Nodo() {
        this.elemento = elemento;
        this.successivo = successivo;
    }

    public int getElemento() {
        return elemento;
    }

    public void setElemento(int elemento) {
        this.elemento = elemento;
    }

    public Nodo getSuccessivo() {
        return successivo;
    }

    public void setSuccessivo(Nodo successivo) {
        this.successivo = successivo;
    }
}
